/**
 * 比较字符串
 */
class eg_3_11 {
    public static void main(String args[]) {
        String s1 = "Hello";
        String s2 = "Hello";
        String s3 = "Good-bye";
        String s4 = "HELLO";
        
        System.out.println(s1 + " equals " + s2 + ": " + s1.equals(s2));// true
        System.out.println(s1 + " equals " + s3 + " : " + s1.equals(s3));// false
        System.out.println(s1 + " equals " + s4 + " : " + s1.equals(s4));// false
        System.out.println(s1 + " equalsIgnoreCase " + s4 + ": " + s1.equalsIgnoreCase(s4));// true，忽略大小写
        System.out.println(s1 + " == " + s2 + ": " + (s1 == s2));// false，==为对象比较，
    }// s1与s2不是一个对象，所以2不相等。
}
